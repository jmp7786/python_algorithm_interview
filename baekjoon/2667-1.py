class House:
    count = 0

    def findHouses(self, s):
        result = []
        for i in range(len(s)):
            for j in range(len(s[0])):
                if self.__dfs(s, i, j):
                    result.append(self.count)
                    self.count = 0

        return sorted(result)

    def __dfs(self, s, i, j):
        if i < 0 or i > len(s) - 1 or j < 0 or j > len(s[0]) - 1 or s[i][j] != 1:
            return False

        s[i][j] = 0
        self.count += 1

        self.__dfs(s, i - 1, j)
        self.__dfs(s, i + 1, j)
        self.__dfs(s, i, j - 1)
        self.__dfs(s, i, j + 1)

        return True


